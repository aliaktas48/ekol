#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	d;
	int	c;

	if (*b != '0')
	{
		d = *a / *b;
		c = *a % *b;
		*a = d;
		*b = c;
	}
}
